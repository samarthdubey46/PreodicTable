def getTable():
    import requests
    from bs4 import BeautifulSoup
    print("Dowloading Table.....")
    url = "https://www.vedantu.com/chemistry/118-elements-and-their-symbols-and-atomic-numbers"
    text = requests.get(url).text
    soup = BeautifulSoup(text,'html.parser')
    table = soup.find("table").find("tbody")
    table = table.find_all('tr')
    final = ""
    for i in table[1:]:
        for j in i.children:
            final += j.text + '\n'
        final+= '----\n'
    print("Table Downloaded.....")
    print()
    return final

def parseTable(table):
    elements = ['' for i in range(118)]
    shortForms = ['' for i in range(118)]
    table = table.split('----')
    for i in table:
        i = i.strip().split('\n')
        if i[0] == '':
            continue
        index = int(i[1]) - 1
        elements[index] = i[0]
        shortForms[index] = i[2]
    return elements, shortForms
